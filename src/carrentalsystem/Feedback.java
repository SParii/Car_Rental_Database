    public void autoID(){
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
        
            con = DriverManager.getConnection("jdbc:mysql://localhost/carrental","root","");
            Statement s = con.createStatement();
            
            rs = s.executeQuery("select Max(feedback_id)from feedback");
            rs.next();
            rs.getString("Max(feedback_id)");
            
            if(rs.getString("Max(feedback_id)")==null){
                txtfeedbackid.setText("5000");
            }
            else
            {
                long id = Long.parseLong(rs.getString("Max(feedback_id)"));
                id++;
                
                txtfeedbackid.setText(String.format("%03d", id));
            }
            
    }   
            catch (ClassNotFoundException ex) {
            Logger.getLogger(Cars.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Cars.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
    }
    
