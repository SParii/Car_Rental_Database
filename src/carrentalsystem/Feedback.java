    private void btnsubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsubmitActionPerformed
        // TODO add your handling code here:
        
        if (buttonGroup1.getSelection() != null)
        {
            if(rating1.isSelected())
            {
                value1=1;
            }
            else if(rating2.isSelected())
            {
                value1=2;
            }
            else if(rating3.isSelected())
            {
                value1=3;
            }
            else if(rating4.isSelected())
            {
                value1=4;
            }
            else if(rating5.isSelected())
            {
                value1=5;
            }
        }
        int cust_id = Integer.parseInt(txtcustid.getText());
        int feedback_id = Integer.parseInt(txtfeedbackid.getText());
        String review = txtreview.getText();
        
        
        
        try{
            pst = con.prepareStatement("insert into feedback(feedback_id,customer_id,rating,review) values(?,?,?,?)");
            pst.setInt(1, feedback_id);
            pst.setInt(2,cust_id);
            pst.setDouble(3,value1);
            pst.setString(4, review);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Thank you for your feedback!");
            autoID();
            clearFields();
        }
        catch(Exception ex){
            ex.printStackTrace();
        }

    }//GEN-LAST:event_btnsubmitActionPerformed

