    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
           // TODO add your handling code here:
            String rgno = txtRegnum.getText();
            String brand = txtBrand.getText();
            String mod = txtModel.getText();
            String aval = comStatus.getSelectedItem().toString();
           try {   
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/carrental","root","");
            pst = con.prepareStatement("insert into car(reg_num,brand,status,price,model) values(?,?,?,?,?)");
            pst.setInt(1, Integer.parseInt(rgno));
            pst.setString(2, brand);
            pst.setString(3, aval);
            pst.setInt(4, Integer.parseInt(txtPrice.getText()));
            pst.setString(5, mod);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Car added successfully");
            txtBrand.setText("");
            txtModel.setText("");
            comStatus.setSelectedIndex(-1);
            txtPrice.setText("");
            txtBrand.requestFocus();
            autoID();
            table_update();
            con.close();}
          catch (ClassNotFoundException ex) {
            Logger.getLogger(Cars.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Cars.class.getName()).log(Level.SEVERE, null, ex);
        }
    
        
    }//GEN-LAST:event_btnSaveActionPerformed

