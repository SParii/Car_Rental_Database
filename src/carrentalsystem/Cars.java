    public void table_update() {
        try {
            int c;
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/carrental","root","");
            pst = con.prepareStatement("select * from car");
            ResultSet Rs = pst.executeQuery();
            ResultSetMetaData rd = Rs.getMetaData();
            c=rd.getColumnCount();
            DefaultTableModel df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(Rs.next())
            {
              Vector v2 = new Vector();
              for(int i=1; i<=c; i++)
              {
                  v2.add(Rs.getInt("reg_num"));
                  v2.add(Rs.getString("brand"));
                  v2.add(Rs.getString("model"));
                  v2.add(Rs.getString("status"));
                  v2.add(Rs.getInt("price"));
              }
              df.addRow(v2);
            
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Cars.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Cars.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
    
    
    
    
    
    public void autoID(){
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
        
            con = DriverManager.getConnection("jdbc:mysql://localhost/carrental","root","");
            Statement s = con.createStatement();
            
            ResultSet rs = s.executeQuery("select Max(reg_num)from car");
            rs.next();
            rs.getString("Max(reg_num)");
            
            if(rs.getString("Max(reg_num)")==null){
                txtRegnum.setText("0001");
            }
            else
            {
                long id = Long.parseLong(rs.getString("Max(reg_num)"));
                id++;
                
                txtRegnum.setText("0" + String.format("%03d", id));
            }
            
    }   
            catch (ClassNotFoundException ex) {
            Logger.getLogger(Cars.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Cars.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        
    }
    
    
    
    
